/*
	31. Визначити мінімальну вартість закупку книг в таблиці покупок. Якщо отримана вартість в діапазоні від 200 до 300, 
	то нічого не повідомляти, в противному випадку вивести повідомлення «Мінімальна вартість закупки = …» 
	(на місці  точок вказати точну вартість).
*/

USE DB_Books

DECLARE @minCost money

SELECT @minCost=MIN(Cost) FROM Purchases
PRINT @minCost

IF (@minCost < 200) OR (@minCost > 300) 
	PRINT 'Мінімальна вартість закупки = ' + CAST(@minCost AS NVarChar(10))